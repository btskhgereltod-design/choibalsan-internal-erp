async function seedKb() {
  for (const a of KB_SEED_ARTICLES) {
    await run(
      `INSERT INTO kb_articles
        (id,module,category,title,body,keywords,role_min,active,sort_order)
       VALUES(?,?,?,?,?,?,?,?,?)
       ON CONFLICT(id) DO NOTHING`,
      [a.id, a.module, a.category, a.title, a.body, a.keywords, a.role_min, 1, a.sort_order]
    );
  }
}

if (require.main === module) {
  seedKb()
    .then(() => {
      console.log(`Seeded ${KB_SEED_ARTICLES.length} KB articles`);
      process.exit(0);
    })
    .catch((e) => {
      console.error(e);
      process.exit(1);
    });
}

